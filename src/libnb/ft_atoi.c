#include "../libft.h"

int	ft_atoi(const char *s)
{
	int	sign;
	int	num;

	sign = 1;
	num = 0;
	while (*s == ' ' || *s == '\n' || *s == '\t'
		|| *s == '\v' || *s == '\f' || *s == '\r')
	s++;
	if (*s == '+' || *s == '-')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	while (ft_isdigit(*s))
	{
		num = num * 10 + (*s - '0');
		s++;
	}
	num *= sign;
	return (num);
}
